package day1;

public class LiteralTest1 {

	public static void main(String[] args) {
		System.out.println(1);
		System.out.println('1');
		System.out.println("1");
		
		System.out.println();
		System.out.println(1+1); // 정수형 리터럴 덧셈
		System.out.println('1'+1); // 정수형, 문자(아스키) 리터럴 덧셈
		System.out.println("1"+1); // 문자열 결합 연산 (concat)
		
	}

}
