package day2;

public class LiteralTest2_1 {
	public static void main(String[] args) {
		System.out.print(10); // int
		System.out.print(-10); // int
		System.out.print(10.0); // double
		System.out.print(10.0f); // float
		System.out.print(10L); // long
		System.out.print(true); // boolean
		System.out.print(false); // boolean
		System.out.print('A'); // char
		System.out.print("A"); // String -> String 리터럴 독특해~ 객체지만, 상수풀에 저장된다.
		System.out.print('가'); // char
		System.out.print("가"); // String
		System.out.print("ABC"); // String
		System.out.print("가나다"); // String
	}
}
