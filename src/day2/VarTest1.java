package day2;

public class VarTest1 {

	public static void main(String[] args) {
		System.out.println(10); // 표준출력장치(콘솔, 화면)로 출력해라.
		System.out.println(123 + 10);
		System.out.println(123 - 10);
		System.out.println(123 * 10);
		System.out.println(123 / 10);
		System.out.println(123 % 10);
		System.out.println("123" + 10); //문자열 결합
	}
}
