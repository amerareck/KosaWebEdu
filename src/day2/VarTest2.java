package day2;

public class VarTest2 {

	public static void main(String[] args) {
		int num = 10;
		
		System.out.println(num); // 표준출력장치(콘솔, 화면)로 출력해라.
		System.out.println(123 + num);
		System.out.println(123 - num);
		System.out.println(123 * num);
		System.out.println(123 / num);
		System.out.println(123 % num);
		System.out.println("123" + num); //문자열 결합
	}
}
